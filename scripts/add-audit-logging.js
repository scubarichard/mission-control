// ── get_client_info — convert to Code tool with audit logging ────────
const CLIENT_INFO_CODE = [
  'var https = require("https");',
  '',
  AUDIT_HELPER,
  '',
  'function get(hostname, path, hdrs) {',
  '  return new Promise(function(resolve) {',
  '    https.get({ hostname: hostname, path: path, headers: hdrs || {} }, function(res) {',
  '      var d = ""; res.on("data", function(c) { d += c; });',
  '      res.on("end", function() { try { resolve(JSON.parse(d)); } catch(e) { resolve({}); } });',
  '    }).on("error", function() { resolve({}); });',
  '  });',
  '}',
  '',
  'var input = $input.all()[0].json || {};',
  'var clientName = (input.query || input.clientName || input.name || "").trim();',
  'if (!clientName) return "Please specify a client name.";',
  '',
  'var wbData = await get("api.crmworkspace.com", "/v1/contacts?per_page=250", { "ACCESS_TOKEN": process.env.WEALTHBOX_TOKEN });',
  'var contacts = wbData.contacts || [];',
  'var contact = null;',
  'var lcName = clientName.toLowerCase();',
  'for (var i = 0; i < contacts.length; i++) {',
  '  if (contacts[i].name && contacts[i].name.toLowerCase().indexOf(lcName) >= 0) { contact = contacts[i]; break; }',
  '}',
  'if (!contact) return "Client \\"" + clientName + "\\" not found in Wealthbox.";',
  '',
  'var bg = contact.background_info || "";',
  'var risk = contact.risk_tolerance || "Not specified";',
  'var obj = contact.investment_objective || "Not specified";',
  'var hor = contact.time_horizon || "Not specified";',
  'var tagList = (contact.tags || []).map(function(t) { return typeof t === "object" ? t.name : String(t); });',
  'var acct = "";',
  'var cf = contact.custom_fields || [];',
  'for (var j = 0; j < cf.length; j++) { if (cf[j].name === "account_number") { acct = cf[j].value; break; } }',
  '',
  '// Fetch notes',
  'var notesData = await get("api.crmworkspace.com", "/v1/notes?contact_id=" + contact.id + "&per_page=5", { "ACCESS_TOKEN": process.env.WEALTHBOX_TOKEN });',
  'var notes = notesData.status_updates || [];',
  'var noteContent = "";',
  'for (var k = 0; k < notes.length; k++) {',
  '  var nTags = notes[k].tags || [];',
  '  for (var l = 0; l < nTags.length; l++) {',
  '    if ((nTags[l].name || nTags[l]) === "Meeting") { noteContent = notes[k].content || ""; break; }',
  '  }',
  '  if (noteContent) break;',
  '}',
  'if (!noteContent && notes.length > 0) noteContent = notes[0].content || "";',
  '',
  '// Audit log',
  'auditLog(contact.id, "Client Profile Accessed", "Client: " + contact.name + " | Acct: " + (acct || "N/A"), {});',
  '',
  '// Format response',
  'var lines = [',
  '  contact.name,',
  '  "Account: " + (acct || "N/A"),',
  '  "Risk: " + risk + " | Objective: " + obj + " | Horizon: " + hor,',
  '  "Interests: " + (tagList.join(", ") || "None"),',
  '  bg ? "Background: " + bg.substring(0, 300) : "",',
  '  noteContent ? "\\nRecent Notes: " + noteContent.substring(0, 400) : "",',
  '].filter(function(l) { return l; });',
  '',
  'return lines.join("\\n");',
].join('\n');

// ── list_clients — add audit logging for filtered searches ───────────
const LIST_CODE = [
  'var https = require("https");',
  '',
  AUDIT_HELPER,
  '',
  'var input = $input.all()[0].json || {};',
  'var searchTerm = (input.query || input.searchTerm || input.filter || "").toLowerCase().trim();',
  '',
  'var contacts = await new Promise(function(resolve) {',
  '  https.get({ hostname: "api.crmworkspace.com", path: "/v1/contacts?per_page=250", headers: { "ACCESS_TOKEN": process.env.WEALTHBOX_TOKEN } }, function(res) {',
  '    var data = ""; res.on("data", function(c) { data += c; });',
  '    res.on("end", function() { try { resolve(JSON.parse(data).contacts || []); } catch(e) { resolve([]); } });',
  '  }).on("error", function() { resolve([]); });',
  '});',
  '',
  'var filtered = contacts;',
  'if (searchTerm) {',
  '  filtered = contacts.filter(function(c) {',
  '    var tags = (c.tags || []).map(function(t) { return (typeof t === "object" ? t.name : String(t)).toLowerCase(); });',
  '    var all = (c.name || "").toLowerCase() + " " + (c.background_info || "").toLowerCase() + " " + (c.risk_tolerance || "").toLowerCase() + " " + (c.investment_objective || "").toLowerCase() + " " + tags.join(" ");',
  '    return all.indexOf(searchTerm) >= 0;',
  '  });',
  '}',
  '',
  'var getAcct = function(cf) { var f = (cf || []).find(function(x) { return x.name === "account_number"; }); return f ? f.value : "N/A"; };',
  '',
  'var list = filtered.map(function(c) {',
  '  var tags = (c.tags || []).map(function(t) { return typeof t === "object" ? t.name : String(t); });',
  '  return "- " + c.name + " | Acct: " + getAcct(c.custom_fields) + " | Risk: " + (c.risk_tolerance || "N/A") + " | Obj: " + (c.investment_objective || "N/A") + (tags.length ? " | Tags: " + tags.join(", ") : "");',
  '}).join("\\n");',
  '',
  '// Audit log for filtered searches only',
  'if (searchTerm && filtered.length > 0) {',
  '  auditLog(filtered[0].id, "Client List Searched", "Filter: " + searchTerm + " | Found: " + filtered.length, {});',
  '}',
  '',
  'var result = filtered.length > 0',
  '  ? "Found " + filtered.length + " of " + contacts.length + " clients" + (searchTerm ? " matching \\"" + searchTerm + "\\"" : "") + ":\\n\\n" + list',
  '  : "No clients found matching \\"" + searchTerm + "\\". Total: " + contacts.length;',
  '',
  'return result;',
].join('\n');

async function deploy() {
  console.log('Fetching router...');
  const resp = await fetch(`${N8N_URL}/api/v1/workflows/${WF_ID}`, {
    headers: { 'X-N8N-API-KEY': N8N_KEY }
  });
  const wf = await resp.json();
  console.log('  Nodes:', wf.nodes.length);

  // 1. Convert get_client_info from HTTP tool to Code tool
  const clientTool = wf.nodes.find(n => n.name === 'Client Lookup Tool');
  clientTool.type = '@n8n/n8n-nodes-langchain.toolCode';
  clientTool.typeVersion = 1;
  clientTool.parameters = {
    name: 'get_client_info',
    description: 'Looks up a specific client in Wealthbox CRM by name. Returns risk profile, investment objective, time horizon, interests, background, and recent meeting notes. Also logs the access to Wealthbox for compliance.',
    jsCode: CLIENT_INFO_CODE
  };
  // Remove credentials if any
  delete clientTool.credentials;
  console.log('  Converted get_client_info to Code tool with audit logging');

  // 2. Update list_clients with audit logging
  const listTool = wf.nodes.find(n => n.name === 'List Clients Tool');
  listTool.parameters.jsCode = LIST_CODE;
  console.log('  Updated list_clients with audit logging');

  // Push
  const r = await fetch(`${N8N_URL}/api/v1/workflows/${WF_ID}`, {
    method: 'PUT',
    headers: { 'Content-Type': 'application/json', 'X-N8N-API-KEY': N8N_KEY },
    body: JSON.stringify({ name: wf.name, nodes: wf.nodes, connections: wf.connections, settings: wf.settings })
  });
  const result = await r.json();
  if (!result.id) { console.error('FAILED:', JSON.stringify(result).substring(0, 500)); process.exit(1); }
  await fetch(`${N8N_URL}/api/v1/workflows/${WF_ID}/activate`, {
    method: 'POST', headers: { 'X-N8N-API-KEY': N8N_KEY }
  });
  console.log('  Activated');
}

deploy().catch(e => { console.error('ERROR:', e.message); process.exit(1); });
