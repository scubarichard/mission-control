const LOG_CODE = `var https = require("https");
var data = $input.all()[0].json;
var results = data.results || [];
var toLog = results.filter(function(r) { return r.success && r.contactId; });

function postNote(contactId, clientName, pv, ytd) {
  return new Promise(function(resolve) {
    var ts = new Date().toLocaleString("en-US", { timeZone: "America/Chicago" });
    var body = JSON.stringify({
      content: "[DAX] Quarterly Review Generated\\nDate: " + ts + "\\nAdvisor: Brett Stone\\nClient: " + clientName + "\\nPortfolio: " + pv + " | YTD: " + ytd + "\\nGenerated by DAX v0.5.0",
      linked_to: [{ id: contactId, type: "Contact" }],
      tags: ["DAX", "Automated"]
    });
    var req = https.request({
      hostname: "api.crmworkspace.com", path: "/v1/notes", method: "POST",
      headers: { "ACCESS_TOKEN": "${WB_KEY}", "Content-Type": "application/json", "Content-Length": Buffer.byteLength(body) }
    }, function(res) {
      var d = ""; res.on("data", function(c) { d += c; });
      res.on("end", function() { resolve(d); });
    });
    req.on("error", function(e) { resolve("err:" + e.message); });
    req.write(body); req.end();
  });
}

var promises = toLog.map(function(r) { return postNote(r.contactId, r.clientName, r.portfolioValue, r.ytdReturn); });
var logResults = await Promise.all(promises);
console.log("Audit: " + logResults.length + " notes logged");

return [{ json: data }];`;

async function deploy() {
  console.log('Fetching...');
  const resp = await fetch(`${N8N_URL}/api/v1/workflows/${WF_ID}`, {
    headers: { 'X-N8N-API-KEY': N8N_KEY }
  });
  const wf = await resp.json();

  // Fix Aggregate Results
  const agg = wf.nodes.find(n => n.name === 'Aggregate Results');
  agg.parameters.jsCode = AGG_CODE;

  // Fix Log Audit
  let logNode = wf.nodes.find(n => n.name === 'Log Audit');
  if (!logNode) {
    logNode = {
      parameters: { mode: 'runOnceForAllItems', jsCode: LOG_CODE },
      id: 'log-audit-001', name: 'Log Audit',
      type: 'n8n-nodes-base.code', typeVersion: 2, position: [2400, 300]
    };
    wf.nodes.push(logNode);
    wf.connections['Aggregate Results'] = { main: [[{ node: 'Log Audit', type: 'main', index: 0 }]] };
    wf.connections['Log Audit'] = { main: [[{ node: 'Respond to Webhook', type: 'main', index: 0 }]] };
    console.log('  Added Log Audit node');
  } else {
    logNode.parameters.jsCode = LOG_CODE;
    console.log('  Updated Log Audit code');
  }

  const r = await fetch(`${N8N_URL}/api/v1/workflows/${WF_ID}`, {
    method: 'PUT',
    headers: { 'Content-Type': 'application/json', 'X-N8N-API-KEY': N8N_KEY },
    body: JSON.stringify({ name: wf.name, nodes: wf.nodes, connections: wf.connections, settings: wf.settings })
  });
  const result = await r.json();
  if (!result.id) { console.error('FAILED:', JSON.stringify(result).substring(0, 500)); process.exit(1); }
  await fetch(`${N8N_URL}/api/v1/workflows/${WF_ID}/activate`, {
    method: 'POST', headers: { 'X-N8N-API-KEY': N8N_KEY }
  });
  console.log('  Activated');
}

deploy().catch(e => { console.error('ERROR:', e.message); process.exit(1); });
