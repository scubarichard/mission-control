// Code node: scans for triggers, outputs { sseBody, _flagged, _noteContent }
const FLAG_CHECK_CODE = `
var response = $input.all()[0].json;
var userMsg = response._userMessage || "";
var daxResp = response._agentOutput || "";
var combinedText = (userMsg + " " + daxResp).toLowerCase();

var triggers = [
  { phrase: "should i buy", severity: "HIGH", reason: "Potential investment recommendation request" },
  { phrase: "should i sell", severity: "HIGH", reason: "Potential investment recommendation request" },
  { phrase: "i recommend", severity: "HIGH", reason: "Explicit recommendation language" },
  { phrase: "best investment", severity: "HIGH", reason: "Investment recommendation language" },
  { phrase: "will go up", severity: "HIGH", reason: "Price prediction language" },
  { phrase: "will go down", severity: "HIGH", reason: "Price prediction language" },
  { phrase: "guarantee", severity: "HIGH", reason: "Prohibited guarantee language" },
  { phrase: "guaranteed return", severity: "CRITICAL", reason: "Prohibited guarantee of returns" },
  { phrase: "risk-free", severity: "CRITICAL", reason: "Prohibited risk-free claim" },
  { phrase: "outperform", severity: "MEDIUM", reason: "Performance claim language" },
  { phrase: "promise", severity: "MEDIUM", reason: "Promise language" },
  { phrase: "insider", severity: "CRITICAL", reason: "Potential insider information reference" },
  { phrase: "complaint", severity: "MEDIUM", reason: "Client complaint signal" },
  { phrase: "lawsuit", severity: "HIGH", reason: "Legal action reference" },
  { phrase: "legal action", severity: "HIGH", reason: "Legal action reference" },
  { phrase: "withdraw all", severity: "MEDIUM", reason: "Client distress signal" },
  { phrase: "switch everything", severity: "MEDIUM", reason: "Potential churning signal" },
  { phrase: "move everything", severity: "MEDIUM", reason: "Potential churning signal" },
  { phrase: "market timing", severity: "MEDIUM", reason: "Market timing language" },
  { phrase: "beat the market", severity: "MEDIUM", reason: "Performance claim language" }
];

var matches = triggers.filter(function(t) { return combinedText.indexOf(t.phrase) >= 0; });
var output = { sseBody: response.sseBody, _flagged: false };

if (matches.length > 0) {
  var severity = matches.some(function(m) { return m.severity === "CRITICAL"; }) ? "CRITICAL" :
                 matches.some(function(m) { return m.severity === "HIGH"; }) ? "HIGH" : "MEDIUM";
  var flagDetails = matches.map(function(m) { return "[" + m.severity + '] "' + m.phrase + '" — ' + m.reason; }).join("\\n");
  var ts = new Date().toLocaleString("en-US", { timeZone: "America/Chicago" });
  output._flagged = true;
  output._noteContent = "[DAX-REVIEW] Compliance Flag — " + severity + "\\nDate: " + ts + "\\nAdvisor: Demo Advisor\\nStatus: PENDING\\n\\nTRIGGERS:\\n" + flagDetails + "\\n\\nUSER: " + userMsg.substring(0, 300) + "\\n\\nDAX: " + daxResp.substring(0, 300) + "\\n\\nGenerated by DAX v0.5.0";
  output._severity = severity;
}

return [{ json: output }];
`.trim();

async function deploy() {
  console.log('Fetching DAX Router workflow...');
  const resp = await fetch(`${N8N_URL}/api/v1/workflows/${WF_ID}`, {
    headers: { 'X-N8N-API-KEY': N8N_KEY }
  });
  const wf = await resp.json();
  console.log('  Nodes:', wf.nodes.length);

  // 1. Update Format Response to pass user message
  const formatNode = wf.nodes.find(n => n.name === 'Format Response');
  if (formatNode) {
    const code = formatNode.parameters.jsCode || '';
    if (code.indexOf('_userMessage') < 0) {
      formatNode.parameters.jsCode = code.replace(
        'return [{ json: { sseBody } }];',
        `const _webhookInput = $('Webhook').first().json;
const _body = _webhookInput.body || _webhookInput;
const _msgs = _body.messages || [];
const _userMessage = _msgs.length > 0 ? (_msgs[_msgs.length - 1].content || '') : (_body.message || _body.text || '');
return [{ json: { sseBody, _userMessage, _agentOutput: agentOutput } }];`
      );
      console.log('  Updated Format Response');
    }
  }

  // 2. Remove old flag check nodes if they exist (clean slate)
  const oldNodes = ['Compliance Flag Check', 'Flag IF', 'Flag Post to Wealthbox'];
  for (const name of oldNodes) {
    const idx = wf.nodes.findIndex(n => n.name === name);
    if (idx >= 0) {
      wf.nodes.splice(idx, 1);
      delete wf.connections[name];
      console.log(`  Removed old node: ${name}`);
    }
  }

  const respondNode = wf.nodes.find(n => n.name === 'Respond');
  const fmtPos = formatNode.position;
  const rspPos = respondNode.position;

  // 3. Add: Flag Check (Code node)
  wf.nodes.push({
    id: 'flag-check-' + Date.now(),
    name: 'Compliance Flag Check',
    type: 'n8n-nodes-base.code',
    typeVersion: 2,
    position: [fmtPos[0] + 170, rspPos[1]],
    parameters: { jsCode: FLAG_CHECK_CODE }
  });

  // 4. Add: IF node (check _flagged)
  wf.nodes.push({
    id: 'flag-if-' + Date.now(),
    name: 'Flag IF',
    type: 'n8n-nodes-base.if',
    typeVersion: 2,
    position: [fmtPos[0] + 340, rspPos[1]],
    parameters: {
      conditions: {
        options: { caseSensitive: true, leftValue: '' },
        conditions: [{ leftValue: '={{ $json._flagged }}', rightValue: true, operator: { type: 'boolean', operation: 'equals' } }],
        combinator: 'and'
      }
    }
  });

  // 5. Add: HTTP Request node (post to Wealthbox)
  wf.nodes.push({
    id: 'flag-post-' + Date.now(),
    name: 'Flag Post to Wealthbox',
    type: 'n8n-nodes-base.httpRequest',
    typeVersion: 4.2,
    position: [fmtPos[0] + 510, rspPos[1] - 80],
    parameters: {
      method: 'POST',
      url: 'https://api.crmworkspace.com/v1/notes',
      sendHeaders: true,
      headerParameters: {
        parameters: [
          { name: 'ACCESS_TOKEN', value: WB_KEY },
          { name: 'Content-Type', value: 'application/json' }
        ]
      },
      sendBody: true,
      contentType: 'raw',
      rawContentType: 'application/json',
      body: '={{ JSON.stringify({content: $json._noteContent, tags: ["DAX", "DAX-REVIEW", $json._severity]}) }}',
      options: {}
    }
  });

  // 6. Wire connections
  // Format Response → Flag Check
  wf.connections['Format Response'] = { main: [[{ node: 'Compliance Flag Check', type: 'main', index: 0 }]] };
  // Flag Check → Flag IF
  wf.connections['Compliance Flag Check'] = { main: [[{ node: 'Flag IF', type: 'main', index: 0 }]] };
  // Flag IF true → Flag Post to Wealthbox, Flag IF false → Respond
  wf.connections['Flag IF'] = {
    main: [
      [{ node: 'Flag Post to Wealthbox', type: 'main', index: 0 }],
      [{ node: 'Respond', type: 'main', index: 0 }]
    ]
  };
  // Flag Post to Wealthbox → Respond
  wf.connections['Flag Post to Wealthbox'] = { main: [[{ node: 'Respond', type: 'main', index: 0 }]] };

  console.log('  Added Flag Check → IF → HTTP Request → Respond flow');

  // Push
  const r = await fetch(`${N8N_URL}/api/v1/workflows/${WF_ID}`, {
    method: 'PUT',
    headers: { 'Content-Type': 'application/json', 'X-N8N-API-KEY': N8N_KEY },
    body: JSON.stringify({ name: wf.name, nodes: wf.nodes, connections: wf.connections, settings: wf.settings })
  });
  const result = await r.json();
  if (!result.id) { console.error('FAILED:', JSON.stringify(result).substring(0, 500)); process.exit(1); }
  console.log('  Workflow updated');

  await fetch(`${N8N_URL}/api/v1/workflows/${WF_ID}/activate`, {
    method: 'POST', headers: { 'X-N8N-API-KEY': N8N_KEY }
  });
  console.log('  Activated');
  console.log('Done — compliance flagging wired into DAX Router');
}

deploy().catch(e => { console.error('ERROR:', e.message); process.exit(1); });
